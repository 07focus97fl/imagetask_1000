// Helper functions
export const getTimepoints = (study: StudyStructure): string[] => {
  return Object.keys(study.timepoints).sort();
};

export const getCouplesByTimepoint = (study: StudyStructure, timepoint: string): string[] => {
  return study.timepoints[timepoint] || [];
};

export const getConversationsForCouple = (timepoint: string, coupleCode: string): Array<{id: string, number: number, displayName: string}> => {
  return [1, 2, 3, 4].map(num => ({
    id: `${timepoint.toLowerCase()}_${coupleCode}_c${num}`,
    number: num,
    displayName: `${timepoint}/${coupleCode}_C${num}`
  }));
}; 